
import { describe, test, beforeEach } from "vitest";
import { Board } from "../src/Board";
import { Tetromino } from "../src/Tetromino";
import { expect } from "chai";

function fallToBottom(board) {
  for (let i = 0; i < 19; i++) {
    board.tick();
  }
}

function moveToRightEdge(board){
  for(let i = 0; i<19; i++) {
    board.moveRight();
  }
}
function moveToLeftEdge(board){
  for(let i = 0; i<19; i++) {
    board.moveLeft();
  }
}

describe("Test Rotating I shape", () =>{
    let board;
    beforeEach(()=>{
        board = new Board(15,10);
    })
    
    test("Test Dropping the I shape to a bigger board", ()=>{
      board.drop(Tetromino.I_SHAPE)
      expect(board.toString()).to.equalShape(
        `.....IIII......
         ...............
         ...............
         ...............
         ...............
         ...............
         ...............
         ...............
         ...............
         ...............`
      )
        
    })

    test("Test rotating the I shape right", () =>{
      board.drop(Tetromino.I_SHAPE)
      board.rotateRight()
      expect(board.toString()).to.equalShape(
        `.......I.......
         .......I.......
         .......I.......
         .......I.......
         ...............
         ...............
         ...............
         ...............
         ...............
         ...............`
      )

      
    })

    test("The I shape can be moved horizontally at the last second", () =>{
      board.drop(Tetromino.I_SHAPE)
      board.rotateRight()
      board.moveDown().moveDown().moveDown().moveDown().moveDown().moveDown()
      board.rotateRight()
      expect(board.toString()).to.equalShape(
        `...............
         ...............
         ...............
         ...............
         ...............
         ...............
         .....IIII......
         ...............
         ...............
         ...............`
      )
    })


    test("I shape will kick if too close to right wall", () =>{
      board.drop(Tetromino.I_SHAPE)
      board.rotateRight()
      moveToRightEdge(board)
      expect(board.toString()).to.equalShape(
        `..............I
         ..............I
         ..............I
         ..............I
         ...............
         ...............
         ...............
         ...............
         ...............
         ...............`
      )
      board.rotateRight()
      expect(board.toString()).to.equalShape(
        `...........IIII
         ...............
         ...............
         ...............
         ...............
         ...............
         ...............
         ...............
         ...............
         ...............`
      )


      
    })

    test("I shape will kick if too close to left wall", () =>{
      board.drop(Tetromino.I_SHAPE)
      board.rotateRight()
      moveToLeftEdge(board)
      expect(board.toString()).to.equalShape(
        `I..............
         I..............
         I..............
         I..............
         ...............
         ...............
         ...............
         ...............
         ...............
         ...............`
      )
      board.rotateRight()
      expect(board.toString()).to.equalShape(
        `IIII...........
         ...............
         ...............
         ...............
         ...............
         ...............
         ...............
         ...............
         ...............
         ...............`
      )
      

      
    })

    test("The I shape can kick from other I blocks", () =>{
      board.drop(Tetromino.I_SHAPE)
      board.rotateRight()
      fallToBottom(board)

      // after first I piece has landed, its locked coordinates should be stored.
      expect(board.fallenBlocksCoordinatesList).to.include.deep.members([
        {row:6, col:7},
        {row:7, col:7},
        {row:8, col:7},
        {row:9, col:7}
      ])

      board.drop(Tetromino.I_SHAPE)
      board.rotateLeft()
      board.moveLeft()
      board.moveDown().moveDown().moveDown().moveDown().moveDown()
      expect(board.getFallingBlocks()).to.include.deep.members([
        {row:5, col:6},
        {row:6, col:6},
        {row:7, col:6},
        {row:8, col:6}
      ])
      expect(board.toString()).to.equalShape(
        `...............
         ...............
         ...............
         ...............
         ...............
         ......I........
         ......II.......
         ......II.......
         ......II.......
         .......I.......`)
      board.tick()
      expect(board.toString()).to.equalShape(
        `...............
         ...............
         ...............
         ...............
         ...............
         ...............
         ......II.......
         ......II.......
         ......II.......
         ......II.......`)
      board.rotateRight()
      /*expect(board.getFallingBlocks()).to.include.deep.members([
        {row:5, col:6},
        {row:6, col:6},
        {row:7, col:6},
        {row:8, col:6}
      ])*/
      expect(board.toString()).to.equalShape(
        `...............
         ...............
         ...............
         ...............
         ...............
         ...............
         ...IIIII.......
         .......I.......
         .......I.......
         .......I.......`
      )
      /*board.tick()
      expect(board.toString()).to.equalShape(
        `...............
         ...............
         ...............
         ...............
         ...............
         ...............
         .......I.......
         ...IIIII.......
         .......I.......
         .......I.......`
      )*/

    })
    test("The I shape can kick from other blocks", () =>{
      board.drop(Tetromino.O_SHAPE)
      fallToBottom(board)
      board.drop(Tetromino.O_SHAPE)
      fallToBottom(board)
      board.drop(Tetromino.I_SHAPE)
      board.rotateLeft()
      board.moveLeft().moveLeft()
      
      expect(board.toString()).to.equalShape(
        `.....I.........
         .....I.........
         .....I.........
         .....I.........
         ...............
         ...............
         ......OO.......
         ......OO.......
         ......OO.......
         ......OO.......`
      )
      
      board.moveDown().moveDown().moveDown().moveDown().moveDown().moveDown()
      
      expect(board.toString()).to.equalShape(
        `...............
         ...............
         ...............
         ...............
         ...............
         ...............
         .....IOO.......
         .....IOO.......
         .....IOO.......
         .....IOO.......`
      )
      board.rotateRight()
      expect(board.toString()).to.equalShape(
        `...............
         ...............
         ...............
         ...............
         ...............
         ...............
         ..IIIIOO.......
         ......OO.......
         ......OO.......
         ......OO.......`
      )

    })

    
  

})


